<p>What is the progression of dental problems and what is the long-term prevention?</p>
<p>MOUTH IS THE BEST INCUBATOR IN THE WORLD………</p>
<p>It has</p>
<ol>
<li>1. Optimum temperature of 98.4deg F</li>
<li>2. Optimum moisture due to presence of saliva</li>
<li>3. Optimum oxygen because we open our mouth and talk, yawn, breathe etc</li>
<li>4. Billions of bacteria present in the mouth at all times (about 700 species)</li>
<li>5. We provide them (the bacteria) food for about 15-18 hrs a day</li>
</ol>
<p>These bacteria sense the presence of food after about 12 hours and begin to multiply into two every 2 minutes thus increasing their number. They breakdown the food and produce acid to enter the tooth and eat the protein layer of the tooth or dentine and reach the pulp. They become the cause of the cavities and reside inside the tooth cavity. Very large cavities reach the pulp to cause pain or maybe no pain but ROOT CANALS involved!!!!</p>
<p>So, <strong>FIRST</strong> let’s start with how we can prevent cavities</p>
<ol>
<li>1. if we brush twice a day at a gap of 12hours then we can break their multiplication cycle</li>
<li>2. check efficiency of your brush by checking if it is cleaning properly or not</li>
<li>3. Don’t eat sweets at night post dinner</li>
<li>4. don’t drink milk at night before going to bed</li>
<li>5. use either floss or interdental brush or dental wood sticks to clean between teeth as 40% of the tooth structure is between  two teeth.</li>
</ol>
<p><strong>SECOND</strong> let’s talk of early detection</p>
<ul>
<li>-Visit the dentist every 6 months to check and control cavities at a small stage- they are trained to see what you can’t</li>
<li>-Anywhere you notice food getting stuck between two teeth and needing physical removal must ring an alarm bell for you. A small cavity or eventually a root canal treatment</li>
</ul>
<p><strong>THIRD</strong> let’s talk of treatment</p>
<ul>
<li>-Every cavity needs to be cleaned thoroughly and filled only then – or the best filling will fail</li>
<li>-Before filling the dentist should check with a cavity detecting dye if it is clean or not – or the filling will fail</li>
<li>-The filling should be done as the original shape of your tooth and not any other way – or it will fail</li>
<li>-Bleeding of gums need to be controlled before final filling -or it will fail</li>
<li>-If while cleaning the cavity the root canal chamber or pulp is open/ exposed then either MTA or RC treatment becomes mandatory.</li>
</ul>
<p>These a just a few important facts for all those looking in the direction of dental health </p>		
