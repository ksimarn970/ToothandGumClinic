                    <h2>TMD [Temporomandibular Joint Disorder]</h2>
                    <p>Most people feel that the headaches and pain in jab have nothing to do with dentistry, but you will be surprised to know that the cause of the pain can be related to your teeth.</p>
                    <p>What are the possible causes of the temporomandibular joint disorder are: </p>
                     <ul>
                        <li>1. Injury to the jaw, the joint, or muscles of the head and neck, such as from a heavy blow or whiplash.</li>
                        <li>2. Grinding or clenching your teeth.</li>
                        <li>3. Dislocation of the ball and socket.</li>
                        <li>4. Arthritis in the TMJ.</li>
                        <li>5. Stress.</li>
                        
                    </ul>
                    
                    
                    <blockquote>
                      <h2>What are the symptoms of TMD?</h2>
                    <p>Common symptoms include:</p>
                    <ul>
                        <li>1. Pain or tenderness in your face, jaw joint area, neck and shoulders, and in or around the ear when you chew or speak</li>
                        <li>2. Problems when you try to open your mouth wide or reduce mouth opening</li>
                        <li>3. Jaws that get stuck, or locked, in the open -or closed-mouth position</li>
                        <li>4. Clicking, popping, or grating sounds in the jaw joint when you open or close your mouth or chew</li>
                        <li>5. Migraines and headaches</li>
                        <li>6. A tired feeling in your face</li>
                        <li>7. Trouble chewing or a sudden uncomfortable bite</li>
                        <li>8. Swelling on the side of your face</li>
                    </ul>
                    </blockquote>
                    
                    <h2>What treatment options are available for TMD?</h2>
                    <p>Treatment Options for TMD are:</p>
                    <p><strong>Medications:</strong> higher doses of anti-inflammatories to combat pain and swelling. Muscle relaxer to relax your jaw. Or an anti-anxiety medication to relieve stress. </p>
                    <p><strong>A splint or night guard:</strong> plastic mouthpieces fit over your upper and lower teeth, so they don’t touch. They lessen the effects of clenching or grinding and aim to correct the bite by putting your teeth in a more correct position. </p>
                    <p>Dentists can replace missing teeth and use crowns, bridges, or braces to balance the biting surfaces of teeth. </p>
                    <p><strong>Transcutaneous electrical nerve stimulation (TENS):</strong>This therapy uses low-level electrical currents to provide pain relief by relaxing your jaw joint and facial muscles. </p>
                    <p><strong>Ultrasound-</strong> Deep heat is applied to the joint to relieve soreness or improve mobility.</p>
                    <p><strong>Trigger-point </strong> injections are pain medication or anesthesia that is injected into tender facial muscles called “trigger points” to give relief.</p>
                    <p><strong>Radio wave therapy</strong> is done to stimulate the joint, which increases blood flow and eases pain.</p>
                    <p><strong>Low-level laser therapy</strong> will lower pain and inflammation and help the patient move the neck more freely and open the mouth wider.</p>
                    
