                    <h2>What is Gum Disease?</h2>
                    <p>Gum disease or periodontal disease is a bacterial infection in the gums and bone surrounding the teeth. If left untreated, the bacterial infection and resultant inflammation will result in loss of attachment between the gum and the teeth and eat away at the supporting bone. If let go for too long, the teeth become loose and are often lost.</p>
                    
                    <p>Symptoms of Gum Disease:</p>
                     <ul>
                        <li>1. bad breath that will not go away</li>
                        <li>2. red or swollen gums</li>
                        <li>3. bleeding or tender gums</li>
                        <li>4. gums that have pulled away from your teeth </li>
                        <li>5. receding gums or teeth that appear longer than normal</li>
                        <li>6. teeth that have shifted position</li>
                        <li>7. loose teeth</li>
                        <li>8. an abscess coming from below the gum-line </li>
                        <li>9. Receding gums can also be a symptom of gum disease.</li>
                       
                    </ul>
                   <h2>Stages of Gum Disease</h2>
                      <p>The first level of gum disease is known as gingivitis. This bacterial infection of the gums needs to be fully treated with thorough cleaning so that health can be easily restored.</p>
                    
                    <p>The second level is more severe and is known as periodontitis. In the second stage, infection of both the gums and the surrounding bone causes the gums and ligaments to detach from the teeth, resulting in bone loss. In this phase of gum disease, a pocket develops under the gumline between the gums and the teeth. Bacteria accumulates in the pocket and hardens into calculus, which irritates the gums further and accelerates the infection, gum detachment, and bone loss.</p>
                    <p>Minimally invasive treatments performed by periodontists help in the treatment and prevention of gum disease and periodontal disease. The doctors will remove the disease-causing bacteria, restore oral health, and, in many cases, reverse the damage.</p>
                    
                    
