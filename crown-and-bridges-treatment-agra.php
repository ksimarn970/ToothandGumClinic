                    <h2>What is a dental crown? </h2>
                    <p>Also called caps, dental crowns are tooth-shaped covers that dentists place over a damaged or decayed tooth. </p>
                    <p>Crowns can help when a filling can’t strengthen or fill enough of your tooth to make it viable. When used to cover misshapen or badly discolored teeth, dental crowns also help improve your appearance.</p>
                    

                      <blockquote>
                         <p>Crowns made of all metal or porcelain fused to metal are strong and are a good choice for back teeth.</p>
                         
                    </blockquote>
                    
                    <p>Crowns made of all ceramic and porcelain fused to metal look like your regular teeth, making them a better choice for your front teeth.</p>
                     <h2>What is a dental bridge (fixed partial denture)?</h2>
                     <p>A dental bridge is a device dentists use when you’re missing one or more teeth. Bridges help eliminate rotation and shifting of the teeth surrounding your gap.</p>
                     <p>When you have missing teeth, the chances of having gum disease and temporomandibular joint (TMJ) disorders may also increase. Bridges reduce that risk, too.</p>
                     <p>Regardless of the kind of bridge you choose, the dentists cement it with abutments that serve as anchors for your bridge.</p>
                     <p>The most common bridges include: </p>
                     <ul>
                        <li><strong>Traditional:</strong>When you choose a traditional bridge, your dentist removes enamel from your teeth to make room for your bridge.</li>
                        <li><strong>Cantilever:</strong>Cantilever bridges are traditional bridges held down on only one side.</li>
                        <li><strong>Maryland:</strong>Resin-bonded or Maryland bridges offer a more conservative approach to replacing your front teeth.</li>
                        <li><strong>Implant-supported:</strong>Your dentist offers implant-supported bridges when you have more than one missing tooth. This type of bridge feels the most like the natural teeth it replaces.</li>
                    </ul>
                    
                   
